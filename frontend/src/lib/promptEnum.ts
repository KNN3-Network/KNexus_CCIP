export const getPreprocessorByLabel = (str: string) => {
  if (!str) return [];
  if (str == "*****") return ["*****"];
  let pStr: any = [];
  let filtPro: any = preprocessorEnumConfig.filter((t) => {
    return t.value === str;
  });
  if (filtPro.length > 0) {
    pStr = [filtPro[0]["label"]];
  } else {
    preprocessorEnumConfig.map((t: any) => {
      t.children.map((s: any) => {
        if (s.value === str) {
          pStr = [t.label, s.label];
        }
      });
    });
  }
  return pStr;
};

